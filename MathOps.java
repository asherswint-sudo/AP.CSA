public class MathOps{
    public static void main (String[] args) {
        int x = 5;
        int y = 8;
        double m = 6.25;
        System.out.println(x + " " + y + " " + m);
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println((double)x/y);
        System.out.println("x+y is " + x + y);
    }
}
